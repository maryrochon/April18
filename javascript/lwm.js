$("#blue1").hover(function(){
    $(".graph").toggleClass("b1")
 if($("#blue1:hover").length == 1) {
    $("#lwm1").css("opacity", "1")
    }
 else{
    $("#lwm1").css("opacity", "0")
 }
})
